
public class MathsInJava {

	public static void main(String[] args) {
		int radius = 10;

		double areaOfCircle = Math.PI * Math.pow(radius, 2);
		System.out.println("Area of Circle : " + areaOfCircle);

		int x = 20;
		int y = -90;
		
		System.out.println("Absolute value of x : "  + Math.abs(x));
		System.out.println("Absolute value of y : " + Math.abs(y));
		System.out.println("Max Integer : " + Math.abs(Integer.MIN_VALUE));
		System.out.println("Max : " + Math.max(x, y));
		System.out.println("Min : " + Math.min(x, y));
		System.out.println("Round : " + Math.round(3.145343));
		System.out.println("Sqrt Demo : " + Math.sqrt(25));
		System.out.println("Cubert Demo : " + Math.cbrt(27));
		System.out.println("Ceil : " + Math.ceil(3.46));
		System.out.println("floor : " + Math.floor(3.46));
		System.out.println("Log : " + Math.log(10));
		System.out.println("Sine : " + Math.sin(120));
		System.out.println("Exp : " + Math.exp(2.0));
		System.out.println("Increment : " + Math.incrementExact(2));
		System.out.println("Increment : " + Math.decrementExact(1));
		System.out.println("addExact : " + Math.addExact(x, y));
		System.out.println("subtract : " + Math.subtractExact(34, 10));
		System.out.println("rint() : " + Math.rint(4.56));
		System.out.println("random : " + Math.random());
		System.out.println("Remainder : " + Math.IEEEremainder(13, 10));
		System.out.println("");
		
		
		
		
		
		

	}

}
